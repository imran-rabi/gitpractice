#4. You are given two integer variables x and y. You need to perform the following operations:

#p = x + y : Addition
#q = x - y : Subtraction
#r = x * y :Multiplication
#s = x / y : Division
#t = x % y : Modulo

x = int(input("Enter a Number:"))
y = int(input("Enter another Number:"))

#addition
print(f"{x} + {y} = {x+y}")
#Subtraction
print(f"{x} - {y} = {x-y}")
#Multiplication
print(f"{x} * {y} = {x*y}")
#Division
print(f"{x} / {y} = {x/y}")
#Modulo
print(f"{x} % {y} = {x%y}")
